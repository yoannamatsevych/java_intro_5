package conditional_statements.switch_statements;

public class Switch {

    public static void main(String[] args) {

        String day = "tuesday";

        switch (day) {
            case ("monday"):

                System.out.println("meat");
                break;

            case "tuesday":
                System.out.println("diet");

            default:
                System.out.println("     ");
        }
    }
}


