package practice.loopsPractice;

public class ForIPractice {
    public static void main(String[] args) {

        System.out.println("\nHard coded print statements. \n");
        System.out.println("Congrats!!!");
        System.out.println("Congrats!!!");
        System.out.println("Congrats!!!");


        System.out.println("\nForI coded print statements. v1 \n");

        for (int i = 0; i < 3; i++) {
            System.out.println("Congrats!!!");
        }

        System.out.println("\nForI coded print statements. v2 \n");

        for (int i = 1; i <= 3; i++) {
            System.out.println("Congrats!!!");
        }

        System.out.println("\nForI coded print statements. v3 \n");

        for (int i = 3; i > 0; i--) {
            System.out.println("Congrats!!!");
        }

        System.out.println("\nForI coded print statements. v3 \n");

        for (int i = 100; i >= 97; i--) {
            System.out.println("Congrats!!!");
        }

    }
}
