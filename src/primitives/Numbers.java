package primitives;

public class Numbers {
    public static void main(String[] args) {
        System.out.println("\n---------Numbers - byte-short-int-long-------\n");
        byte age = 45;
        int favNumber = 32768;
        long myNumber = 1;
        System.out.println(age);
        System.out.println(favNumber);

        System.out.println("\n---------Numbers - float - double-------\n");
        float balance1 = 23.345565764738348f;
        double balance2 = 23.345547664774664;
        System.out.println(balance1);
        System.out.println(balance2);

        double transaction = 20;

        System.out.println(transaction); // 20.0
    }
}
